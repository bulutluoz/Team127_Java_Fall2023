package day03_WrapperClasses_MatematikselIslemler;

public class C04_IncrementDecrement {

    public static void main(String[] args) {

        int sayi = 20 ;

        sayi = sayi + 5 ;

        System.out.println(sayi); // 25

        // sayiyi 3 artirip yazdirin

        System.out.println( sayi += 3 ); // 28

        System.out.println(sayi); // 28

        // sayinin degerini 2 katina cikarip yazdirin

        System.out.println( sayi *= 2 ); // 56


    }
}
